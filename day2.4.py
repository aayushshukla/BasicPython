n1=10
n2=3
add=n1+n2
sub=n1-n2
mul=n1*n2
div= n1/n2
fdiv=n1//n2  # floor division gives only int value 
power=n1**n2   # n1 raise to power n2
rem=n1%n2    # it will give remainder
print('sum of n1 and n2',add)
print('sub  of n1 and n2',sub)
print(n1,'*',n2,'=',mul)
print(f' {n1}/{n2}={div}')
print(f' {n1}//{n2}={fdiv}')
print(f' {n1}**{n2}={power}')
print(f' {n1}%{n2}={rem}')
